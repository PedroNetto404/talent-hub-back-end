namespace TalentHub.ApplicationCore.Resources.Candidates.Enums;

public enum GenderIdentity
{
    /// <summary>
    /// Pessoa que se identifica como homem.
    /// </summary>
    Male,

    /// <summary>
    /// Pessoa que se identifica como mulher.
    /// </summary>
    Female,

    /// <summary>
    /// Pessoa que não se identifica exclusivamente com os gêneros masculino ou feminino.
    /// </summary>
    NonBinary,

    /// <summary>
    /// Pessoa cuja identidade de gênero flutua entre diferentes gêneros.
    /// </summary>
    GenderFluid,

    /// <summary>
    /// Pessoa que não se identifica com nenhum gênero.
    /// </summary>
    Agender,

    /// <summary>
    /// Pessoa que se identifica com dois gêneros, geralmente masculino e feminino.
    /// </summary>
    Bigender,

    /// <summary>
    /// Pessoa cuja identidade de gênero não se conforma com as normas binárias de gênero.
    /// </summary>
    GenderQueer,

    /// <summary>
    /// Pessoa que não se identifica com as opções apresentadas.
    /// </summary>
    Other,

    /// <summary>
    /// Pessoa que prefere não informar sua identidade de gênero.
    /// </summary>
    PreferNotToSay
}
